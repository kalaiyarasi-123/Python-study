#How to check the data types
#1.string type
x="helloworld"
print(type(x))

#2.Number type
x=4
print(type(x))

#3.float type
x=4.5
print(type(x))

#4.complex type
x=5j
print(type(x))

#5.list type
x = ["apple", "banana", "cherry"]
print(type(x))

#.6 dict type
x = {"name" : "John", "age" : 36}

#display the data type of x:
print(type(x)) 

#.7 set type
x = {"apple", "banana", "cherry"}
print(type(x))